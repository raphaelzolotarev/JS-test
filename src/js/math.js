console.log(Math.abs(-2));          //positif
console.log(Math.ceil(10.23));      //always top
console.log(Math.floor(10.23));     //always bottom
console.log(Math.round(10.50));     //round close to decimal 0.5
console.log(Math.max(5,6));         //highest value
console.log(Math.min(...[7,55]));   //lowest, accept also varargs or list argument ... is caled spread operator
console.log(Math.pow(2,2));         //puissance